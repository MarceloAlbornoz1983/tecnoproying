				<h2 class="fh5co-project-title">Impermeabilización de Estanques</h2>
				<span class="fh5co-project-sub"></span>
				<p>La impermeabilización de los aljibes nos ayuda a evitar fugas de agua y al mismo tiempo prolongar la vida útil de nuestros pozos. Ahora existe una técnica para impermeabilizar con material grado alimenticio garantizando así, la calidad del agua almacenada.
					Sabemos que su incorrecto funcionamiento puede conllevar también un coste adicional, derivado del consumo eléctrico de las estaciones de bombeo. Asimismo, esta situación puede afectar al proceso biológico de estaciones depuradoras de aguas residuales.
					En estos casos, la rehabilitación se lleva a cabo mediante la inyección de resinas en el trasdós para el sellado de las filtraciones y el relleno estructural, finalizando con un revestimiento interior mediante morteros especiales de reparación. Utilizamos morteros de fraguado rápido para la formación de medias cañas y sellado de juntas, proporcionando una excelente resistencia y estanqueidad.
				</p>
